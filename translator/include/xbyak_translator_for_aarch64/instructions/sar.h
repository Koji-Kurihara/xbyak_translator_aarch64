#define CG64 Xbyak_aarch64::CodeGenerator
void translateSAR(xed_decoded_inst_t *p) {
  namespace xa = Xbyak_aarch64;
  struct xt_a64fx_operands_structV3_t a64;
  xt_construct_a64fx_operandsV3(p, &a64);
  bool isValid = false;
  xt_reg_idx_t dstIdx = XT_REG_INVALID;
  xed_uint64_t uimm = 0;

  /* Col=T119*/
  if (false ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 64 && true)) {
    XT_UNIMPLEMENTED;
  }
  /* Col=W119*/
  if (false ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true)) {
    dstIdx = a64.operands[0].regIdx;
  }
  /* Col=AB119*/
  if (false ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true)) {
    uimm = a64.operands[1].uimm;
  }
  /* Col=AE119*/
  if (false ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true)) {
    int count = uimm & 63;
    if (count != 0) {
      CG64::mov_imm(X_TMP_3, ~uint64_t(0));
      CG64::asr(X_TMP_1, xa::XReg(dstIdx), uint32_t(uimm - 1));
      CG64::and_(X_TMP_2, X_TMP_1, 1);
      CG64::asr(xa::XReg(dstIdx), X_TMP_1, 1);
      CG64::adds(X_TMP_3, X_TMP_3, X_TMP_2);
    } else if (count == 1) {
      CG64::mov(X_TMP_0, 1);
      CG64::mov_imm(X_TMP_3, ~uint64_t(0));
      CG64::and_(X_TMP_2, xa::XReg(dstIdx), 1);
      CG64::asr(xa::XReg(dstIdx), xa::XReg(dstIdx), 1);
      CG64::adds(X_TMP_3, X_TMP_3, X_TMP_2);
      CG64::setf8(W_TMP_0);
    }
  }
  /* Col=BK119*/
  if (false ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 8 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 16 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[1].opName == XED_OPERAND_REG1 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[1].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 32 && true) ||
      (a64.operands[0].opName == XED_OPERAND_REG0 &&
       a64.operands[0].opWidth == 64 && true) ||
      (a64.operands[0].opName == XED_OPERAND_MEM0 &&
       a64.operands[0].opWidth == 64 && true)) {
    XT_VALID_CHECK;
  }
  XT_VALID_CHECK_IF;
}
#undef CG64
