void Xbyak::CodeGenerator::translateVEXTRACTF64X4(xed_decoded_inst_t *p) {
  namespace xa = Xbyak_aarch64;
  struct xt_a64fx_operands_struct_t a64;
  xt_construct_a64fx_operands(p, &a64);

  /* 2020/03/12 16:22 */
  /* Col=S103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_MEM && a64.srcType == A64_OP_REG && true) ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_ZERO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true) ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_ZERO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_MEM && a64.srcType == A64_OP_REG && true) ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_MERG && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true) ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_MERG && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_MEM && a64.srcType == A64_OP_REG && true)) {
    XT_UNIMPLEMENTED;
  }

  /* Col=V103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    a64.zTmpIdx = xt_push_zreg(&a64);
  }

  /* Col=Y103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    xa_->mov(xa::ZRegD(a64.zTmpIdx), xa::ZRegD(a64.srcIdx));
  }

  /* Col=AA103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    if ((a64.uimm & 0x1) == 0) {
      xa_->mov(xa::ZRegD(a64.dstIdx), xa::ZRegD(a64.zTmpIdx));
    }
  }

  /* Col=AC103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    if ((a64.uimm & 0x1) == 1) {
      xa_->ext(xa::ZRegB(a64.zTmpIdx), xa::ZRegB(a64.zTmpIdx), 32);
    }
  }

  /* Col=AE103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    if ((a64.uimm & 0x1) == 1) {
      xa_->mov(xa::ZRegD(a64.dstIdx), xa::ZRegD(a64.zTmpIdx));
    }
  }

  /* Col=AI103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    xa_->mov(xa::ZReg(a64.dstIdx).s, P_MSB_256 / xa::T_m, 0);
  }

  /* Col=AX103*/
  if (false ||
      (a64.dstWidth == 256 && a64.PredType == A64_PRED_NO && a64.EVEXb == 0 &&
       a64.dstType == A64_OP_REG && a64.srcType == A64_OP_REG && true)) {
    xt_pop_zreg();
  }
}
